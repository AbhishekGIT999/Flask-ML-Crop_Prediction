import pandas as pd
from sklearn.model_selection  import train_test_split
from sklearn.ensemble import RandomForestClassifier

data = pd.read_csv('crop_recommendation_dataset_10000_rows.csv')

data.head()

data.shape

x = data.iloc[:,:-1] #feature
y = data.iloc[:,-1] #labels

x_train,x_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state=42)

x_train.head()

y_train.head()

model = RandomForestClassifier()

model.fit(x_train,y_train)

pickle.dump(model, open('model.pkl', 'wb'))

predictions = model.predict(x_test)

accuracy = model.score(x_test,y_test)

print("accuracy :", accuracy)

train_accuracy = model.score(x_train, y_train)
print("Training Accuracy:", train_accuracy)

print(accuracy)

new_features = [36,58,25,28.66,8.39,159.3,36.9]
predicted_crop = model.predict([new_features])
print("Predicted Crop:", predicted_crop)

"""Comparing the training accuracy to the test accuracy helps us understand if the model is overfitting. If the training accuracy is significantly higher than the test accuracy, it suggests that the model has learned the training data too well and may not generalize effectively to new, unseen data (overfitting)."""